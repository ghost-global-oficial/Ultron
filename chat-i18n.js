// Função para aplicar traduções
function applyTranslations() {
  console.log('=== APLICANDO TRADUÇÕES ===');
  
  // Obter idioma do localStorage
  const savedLanguage = localStorage.getItem('ultron.language') || 'en-US';
  console.log('Idioma detectado:', savedLanguage);
  
  // Se for inglês, não precisa traduzir
  if (savedLanguage === 'en-US') {
    console.log('Idioma é inglês, não precisa traduzir');
    return;
  }
  
  const translations = chatTranslations[savedLanguage];
  if (!translations) {
    console.warn('Traduções não encontradas para:', savedLanguage);
    return;
  }
  
  console.log('Traduções carregadas:', Object.keys(translations).length, 'strings');
  
  // Traduzir textos visíveis
  const translateElement = (element) => {
    // Traduzir texto direto
    if (element.childNodes.length === 1 && element.childNodes[0].nodeType === Node.TEXT_NODE) {
      const originalText = element.textContent.trim();
      if (translations[originalText]) {
        element.textContent = translations[originalText];
      }
    }
    
    // Traduzir placeholders
    if (element.placeholder && translations[element.placeholder]) {
      element.placeholder = translations[element.placeholder];
    }
    
    // Traduzir title
    if (element.title && translations[element.title]) {
      element.title = translations[element.title];
    }
    
    // Traduzir aria-label
    if (element.getAttribute('aria-label')) {
      const ariaLabel = element.getAttribute('aria-label');
      if (translations[ariaLabel]) {
        element.setAttribute('aria-label', translations[ariaLabel]);
      }
    }
  };
  
  // Aplicar traduções em todos os elementos
  const applyToAll = () => {
    // Botões
    document.querySelectorAll('button').forEach(translateElement);
    
    // Links
    document.querySelectorAll('a').forEach(translateElement);
    
    // Labels
    document.querySelectorAll('label').forEach(translateElement);
    
    // Spans
    document.querySelectorAll('span').forEach(translateElement);
    
    // Inputs
    document.querySelectorAll('input').forEach(translateElement);
    
    // Textareas
    document.querySelectorAll('textarea').forEach(translateElement);
    
    // Headings
    document.querySelectorAll('h1, h2, h3, h4, h5, h6').forEach(translateElement);
    
    // Paragraphs
    document.querySelectorAll('p').forEach(translateElement);
    
    // Divs com texto curto (provavelmente labels)
    document.querySelectorAll('div').forEach(element => {
      if (element.textContent.trim().length < 50) {
        translateElement(element);
      }
    });
  };
  
  // Aplicar traduções inicialmente
  applyToAll();
  console.log('✓ Traduções aplicadas');
  
  // Observar mudanças no DOM e reaplicar traduções
  const observer = new MutationObserver((mutations) => {
    let shouldTranslate = false;
    
    for (const mutation of mutations) {
      if (mutation.type === 'childList' && mutation.addedNodes.length > 0) {
        shouldTranslate = true;
        break;
      }
    }
    
    if (shouldTranslate) {
      applyToAll();
    }
  });
  
  observer.observe(document.body, {
    childList: true,
    subtree: true
  });
  
  console.log('✓ Observer de traduções ativado');
}

// Aplicar traduções quando o DOM estiver pronto
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', applyTranslations);
} else {
  applyTranslations();
}

// Reaplicar traduções após 1 segundo (para garantir que a UI carregou)
setTimeout(applyTranslations, 1000);

// Reaplicar traduções após 3 segundos (para componentes que carregam depois)
setTimeout(applyTranslations, 3000);

console.log('✓ Sistema de tradução do chat inicializado');
